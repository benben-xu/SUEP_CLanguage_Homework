//编写程序 c3-2-9.c，程序的功能是：从键盘上输入一个华氏温度，能够输出相应的摄氏温度,输出结果保留2 位小数。
//华氏温度F与摄氏温度 c的转换关系为： C =5 / 9 （ 𝐹 − 32)

#include <stdio.h>
int main( )
{
    //定义需要的变量类型;
    //键盘输入已知华氏温度F;
    //由赋值语句实现转换为华氏温度 c;
    //输出语句;
    float f1,c1;
    printf("请输入华氏温度: ");
    scanf("%f",&f1);
    c1 = 5.0 / 9.0 * (f1 - 32);
    printf("%.2f\n",c1);

    return 0;
}