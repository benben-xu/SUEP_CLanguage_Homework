#include <stdio.h>

/*
 * 1. 常用的类型：float \ double \long double
 *
 * 2. float表示数据的范围要大于long类型表示的范围
 *
 * 3. 浮点型变量不能使用signed或unsigned修饰符。
 *
 * 4. 最长用的浮点类型为：double类型，因为精度比float高。
 *
 * 5. 浮点型常量，默认为double类型。
 *
 * 6. 关于后缀：如果定义float类型的，则需要以"F"或"f"作为后缀。
 *            如果定义long double类型的，则需要以"l"或"L"作为后缀
 */
